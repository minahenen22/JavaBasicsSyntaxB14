package arraysPrep;

public class NestedForLoopPrac {

	public static void main(String[] args) {
		for (int i=0;i<=2;i++) {
			for (int j=0;j<=9;j++) {
				if (i==2 && j>3) {
					break;
				}
				for (int l=0;l<=5;l++) {
					for (int m=0;m<=9;m++) {
						System.out.println(i+""+j+":"+l+m);
				}
			}
			
			
			
			
			
			
			
		}

	}

}
}
